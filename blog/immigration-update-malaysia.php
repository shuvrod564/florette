                <p>Immigration Department’s new requirements for all categories of applications i.e. Employment Pass (EP) Categories I, II & III, Professional Visit Pass (PVP), Dependent Pass (DP) and Long Term Social Pass (LTSP), the Malaysia Digital Economy Corporation (MDEC) has also announced its new requirements and new processes in line with the immigration Department’s functions. The MDEC’s new guidelines are effective 1 Sep 2016. MDEC governs companies in the IT sector and the expats Service Center under MDEC and the Immigration Unit at MDEC process all immigration applications for foreigners. For purposes of this update, below are the EP Categories –</p>
