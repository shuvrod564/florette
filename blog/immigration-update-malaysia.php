                <h2 class="fs-5">1. Passport Validity</h2>
                <p>Effective 1 Sep 2016, any passports submitted for endorsement of the EP, PVP, DP, or LTVP must have a validity of 12 months and above. The endorsement stage is where the passes are affixed to the original passports of the applicant.</p>
                  
                <h2 class="fs-5">2. Entry into Malaysia after the Approval stage & Visa with Reference (VDR)</h2>
                <p>Effective 1 Sep 2016, all applicants under the EP (Categories I, II & III), PVP, DP, or LTVP must now produce the Approval Letter for verification purposes when entering Malaysia to complete the final stage of their application i.e. the endorsement stage (affixing the passes into the passport). The Approval Letter is issued online and it is to be printed and carried along for entry to Malaysia.</p>
                <p>Nationalities who require a prior visa to enter Malaysia must obtain the Visa with Reference (VDR) from the Malaysian Embassies/High Commission/Consulate General abroad using the said Approval Letter. This is mandatory. They will then be granted a Visa to enter Malaysia.</p>
                <p>If an applicant enters Malaysia (for any reason) without the Approval Letter and/or VDR (for nationalities who require a prior visa), the endorsement stage cannot be completed and the applicant must exit and re-enter the country.</p>
                <p>This requirement is also applicable to any resubmission of applications that have been returned to the company by the Authorities.</p>
                <p>In the past, this was not a requirement, and the Approval Letter was only kept by the sponsoring Company for the record.</p>

                <h2 class="fs-5">3. Change of Pass Type & Visa with Reference (VDR)</h2>
                <p>Effective 1 Sep 2016, existing pass holders who wish to change their pass type (whether within the same company or from a different company), must exit and re-enter Malaysia after approval to obtain the VDR at the Malaysian Embassy/High Commission/Consulate General abroad before the new pass type can be endorsed. This VDR process is applicable to nationalities who require a prior visa to enter Malaysia.</p>
                <p class="fw-medium mb-1">The change of pass type applies to:</p>
                <ul class="disc__list"> 
                    <li>EP Category III to EP Category III from one company to another</li>
                    <li>Dependant Pass (DP) to EP Category III</li>
                    <li>Student Pass to EP Category III</li>
                    <li>PVP to EP Category III</li>
                </ul>

                <h2 class="fs-5">4. Cooling-off Period</h2>
                <p>Effective 1 Sep 2016, existing pass holders who wish to change their pass type (whether within the same company or from a different company), must exit Malaysia and serve a 3-month cooling-off period. The new application can be submitted after the pass holder exits the country. As to whether the pass holder can re-enter for social or business reasons while serving the 3-month cooling-off period, the Authorities advised they will confirm at a later date.</p>
                <p class="fw-medium mb-1">The cooling-off period for change of pass type applies to –</p>
                <ul class="disc__list"> 
                    <li>EP Category III to EP Category III from one company to another</li>
                    <li>4th year of EP Category III renewal or change of position</li>
                    <li>Temporary EP (PLKS) to any EP Category (I, II, or III)</li>
                    <li>PVP to any EP Categories I, II, and III</li> 
                </ul>

                <h2 class="fs-5">5. Change of Employer and Release Letter Requirement</h2>
                <p>Effective 1 Sep 2016, existing pass holders who change employment i.e. new employer) must provide the following documents before the application can be made:-</p> 
                <ul class="disc__list">  
                    <li>Cancellation of existing / current EP</li>
                    <li>Release letter from the previous employer</li>
                    <li>In the past, this requirement was not strictly enforced.</li>
                </ul>

                <h2 class="fs-5">6. New Born</h2>
                <p>Effective 1 Sep 2016, children born in Malaysia and who obtain their passport 6 months after the date of birth, must apply for a Special Pass at the Enforcement Unit of Immigration HQ in Putrajaya before the endorsement of the Dependent Pass can be done. The steps are:-</p> 
                <ul class="disc__list">   
                    <li>Obtain acknowledgment letter from e-Xpats Service Center upon submission of Stage 1 (only applicable if EP holder’s application is not yet approved)</li>
                    <li>Obtain memo from Immigration Unit in MDEC</li>
                    <li>Proceed to the Enforcement Unit for the Special Pass application</li>
                    <li>If the newborn’s passport is obtained within 6 months, from the date of birth, no Special Pass is required, i.e. the above process will not be applicable</li>
                </ul>

                <h2 class="fs-5">7. Renewal of Social Visit Pass</h2>
                <p>Effective 1 Sep 2016,</p> 
                <p>Children above 18 years old</p>
                <p>An original letter from the Commissioner of Oaths and a confirmation letter from the applicant’s Embassy in Malaysia with regard to the latest status (whether single, unemployed, under custody or EP holder, etc) must be produced for each renewal.</p>
                <p class="fw-medium mb-2">Common Law Wife</p>
                <p>An original letter from the applicant’s Embassy in Malaysia as proof of the common-law status must be produced for each renewal. At present, this is only required for the first application.</p>
                

